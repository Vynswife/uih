                                            <p class="mb-0">Berikut adalah contoh panduan singkat dalam pengisian laporan keluhan masyarakat di website keluhan masyarakat ke kepolisian:

Langkah 1: Akses Website Keluhan Masyarakat Kepolisian
- Buka browser web Anda dan masuk ke situs resmi kepolisian atau portal khusus keluhan masyarakat.
- Cari opsi atau tab yang mengarahkan Anda ke formulir pengisian laporan keluhan masyarakat.

Langkah 2: Identifikasi Diri Anda
- Isi informasi pribadi Anda yang diminta, seperti nama lengkap, alamat, nomor kontak, dan alamat surel (jika diperlukan).
- Pastikan informasi identitas Anda akurat dan lengkap untuk memudahkan kontak balik dari pihak berwenang.

Langkah 3: Deskripsi Keluhan Masyarakat
- Jelaskan secara rinci keluhan yang Anda miliki.
- Berikan kronologi peristiwa sejelas mungkin, termasuk tanggal, waktu, lokasi, dan semua detail yang relevan.
- Sertakan bukti pendukung jika ada, seperti foto, video, atau dokumen yang dapat memperkuat keluhan Anda.

Langkah 4: Tentukan Kategori Keluhan
- Pilih kategori keluhan yang sesuai dengan sifat masalah yang Anda alami (misalnya, kejahatan, pelanggaran lalu lintas, kekerasan domestik, dll.).
- Pemilihan kategori yang tepat membantu pihak berwenang dalam menangani keluhan Anda dengan efisien.

Langkah 5: Kirim Laporan
- Setelah semua informasi telah diisi dengan lengkap, tinjau kembali keluhan Anda untuk memastikan tidak ada kesalahan atau kekurangan.
- Klik tombol "Kirim" atau "Submit" untuk mengirimkan laporan keluhan Anda ke pihak berwenang.

Langkah 6: Tindak Lanjut
- Setelah mengirimkan laporan, pastikan untuk mencatat nomor referensi atau konfirmasi yang diberikan oleh sistem.
- Jangan ragu untuk menghubungi pihak berwenang menggunakan nomor kontak yang disediakan jika Anda memerlukan informasi lebih lanjut atau ingin mengetahui perkembangan dari keluhan Anda.

